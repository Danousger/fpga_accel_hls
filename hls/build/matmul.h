// ============================================================================
// matmul.h — 定点数矩阵乘法加速器 头文件
// 目标芯片: Xilinx Zynq-7020 (xc7z020clg400-1)
// 定点格式: Q8.8 (1bit符号 + 7bit整数 + 8bit小数)
// ============================================================================

#ifndef MATMUL_H
#define MATMUL_H

#include <ap_fixed.h>
#include <hls_stream.h>
#include <ap_int.h>

// ---------------------------------------------------------------------------
// 定点数类型定义
// ---------------------------------------------------------------------------

// 输入/输出数据类型: Q8.8, 16位宽
//   - 范围: [-128.996, 127.996]
//   - 精度: 1/256 = 0.00390625
typedef ap_fixed<16, 8, AP_RND, AP_SAT>  data_t;

// 累加器类型: 48位宽, 32位小数 (16位整数 → 范围 [-32768, 32768))
//   - ap_fixed<16,8> × ap_fixed<16,8> → 乘积最大 ~2^14
//   - K=16 次累加 → 需要额外 4bit 整数位, 总计需要 ~18bit 整数
//   - 48bit 累加器: 16bit 整数 + 32bit 小数, 足够容纳 K≤64 的点积
typedef ap_fixed<48, 32, AP_RND, AP_SAT> acc_t;

// ---------------------------------------------------------------------------
// 矩阵尺寸约束 (受 BRAM 和 DSP 资源限制)
// ---------------------------------------------------------------------------

// 单次硬件调用支持的最大矩阵尺寸
#define MAX_M   64
#define MAX_N   64
#define MAX_K   64

// 片内 tile 尺寸 (BRAM 缓冲的粒度)
#define TILE_M  16
#define TILE_N  16
#define TILE_K  16

// 并行乘法器数量 (DSP48E 使用量 ≈ PARALLEL_FACTOR * 2 ~ 3)
#define PARALLEL_N  4

// 通用宏
#ifndef MIN
#define MIN(a, b) (((a) < (b)) ? (a) : (b))
#endif

// ---------------------------------------------------------------------------
// AXI Stream 类型
// ---------------------------------------------------------------------------
typedef hls::stream<data_t> data_stream;

// ---------------------------------------------------------------------------
// 控制寄存器地址映射 (AXI Lite)
// ---------------------------------------------------------------------------
// 0x00: CTRL   — [0] start, [1] auto_restart
// 0x04: STATUS — [0] done, [1] idle, [2] ready
// 0x08: M_DIM  — 矩阵 A 的行数
// 0x0C: N_DIM  — 矩阵 B 的列数
// 0x10: K_DIM  — 矩阵 A 的列数 / 矩阵 B 的行数

#endif // MATMUL_H
